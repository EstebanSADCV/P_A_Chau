    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            recursoCompartido recurso = new recursoCompartido(jLabel1);
            RecursoRelevo recursoR = new RecursoRelevo(jLabel1);
   
            a = (int) jSpinner1.getValue();
            CyclicBarrier barrier = new CyclicBarrier(a+1);
            tr = new Corredor[a];
            jPanel1.removeAll();
            jPanel1.setLayout(null);
            JLabel[] Je = new JLabel[a];
            JLabel[] J2 = new JLabel[a];
            for (int i = 0; i < a; i++) {
                Je[i] = new JLabel("Labels" + (i + 1));
                Je[i].setSize(new Dimension(100, 40));
                Je[i].setLocation(0, i * 15);
                jPanel1.add(Je[i]);

                J2[i] = new JLabel("â†’Labels" + (i + 1));
                J2[i].setSize(new Dimension(100, 40));
                J2[i].setLocation(((this.getWidth() - 105) / 2), i * 15);
                jPanel1.add(J2[i]);
            }
            jPanel1.updateUI();
           
            for (int i = 0; i < Je.length; i++) {
                tr[i] = new Corredor(Je[i], J2[i], recurso, recursoR, (this.getWidth() - 105) / 2,barrier);
            }
            for (int i = 0; i < Je.length; i++) {
                tr[i].Start();
            }
    }//GEN-LAST:event_jButton1ActionPerformed

